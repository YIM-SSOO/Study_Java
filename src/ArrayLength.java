public class ArrayLength {

    public static void main(String[] args) {

        String[] days = {"월", "화", "수", "목", "금", "토", "일"};

        //length라는 값은 lengthOfDays에 대입하여 준다.
        int lengthOfDays = days.length;

        // 출력
        System.out.println(lengthOfDays);

        // 결과는 7
    }
}
